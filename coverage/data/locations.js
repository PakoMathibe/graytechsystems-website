// Flatten locations for easy searching
let allLocations = [];
for (const province in locationsDatabase) {
  locationsDatabase[province].areas.forEach((area) => {
    allLocations.push({
      ...area,
      province: locationsDatabase[province].province,
      searchTerms: [
        area.name.toLowerCase(),
        area.suburb.toLowerCase(),
        area.city.toLowerCase(),
        area.postalCode,
        `${area.suburb} ${area.city}`.toLowerCase(),
        `${area.city} ${area.suburb}`.toLowerCase(),
      ],
    });
  });
}

// Search function
function searchLocation(query) {
  if (!query || query.length < 2) return [];
  const lowerQuery = query.toLowerCase();
  return allLocations
    .filter((loc) => loc.searchTerms.some((term) => term.includes(lowerQuery)))
    .slice(0, 10);
}
